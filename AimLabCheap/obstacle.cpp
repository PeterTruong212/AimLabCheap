Obstacle::Obstacle() {
    // Khởi tạo các giá trị mặc định cho position, size, và color
    position = glm::vec3(0.0f);
    size = glm::vec3(2.0f, 1.0f, 1.0f);
    color = glm::vec3(1.0f); // Màu trắng mặc định

    // Tạo hình hộp
    createBox();
}

Obstacle::~Obstacle() {
    glDeleteVertexArrays(1, &VAO);
    glDeleteBuffers(1, &VBO);
    glDeleteProgram(shaderProgram);
}

void Obstacle::Init(const char* vertexShaderPath, const char* fragmentShaderPath) {
    shaderProgram = createShaderProgram(vertexShaderPath, fragmentShaderPath);
}

